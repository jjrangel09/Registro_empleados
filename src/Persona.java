
public abstract class Persona {

	abstract public int getEdad();
	
	abstract public void setEdad(int edad);
	
	abstract public int getAltura();
	
	abstract public void setAltura(int alt);
	
	abstract public void setApellidos(String apellidos);
	
	abstract public String getNombreC();
	
	abstract public void setNombres(String nombres);
	
}
